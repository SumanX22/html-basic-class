// How variables are declared in JS

// 3 METHODS TO declare a variable. let, const and var

// NEVER EVER EVER EVER USE VAR!

// HOW TO declare a variable using VAR

var var1 = "meow";

console.log(typeof var1);
console.log(var1);

var1 = 22;

console.log(typeof var1);
console.log(var1);

var1 = 2.2;

console.log(typeof var1);
console.log(var1);

// var1 = true;

console.log(typeof var1);
console.log(var1);

// HOW TO declare a variable usinG let

let letVariable = "bhao";

console.log(typeof letVariable);
console.log(letVariable);

letVariable = 22;

console.log(typeof letVariable);
console.log(letVariable);

// HOW TO declare a variable using const

const constantVariable = "Naam Udita hain!!";

console.log(typeof constantVariable);
console.log(constantVariable);

constantVariable = "Naam upun ka Piya hain!";
